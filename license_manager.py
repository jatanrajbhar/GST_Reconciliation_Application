        hdr.pack(fill="x")
        hdr.pack_propagate(False)
        ctk.CTkLabel(
            hdr,
            text="Product Activation",
            font=ctk.CTkFont(size=20, weight="bold"),
            text_color="white",
        ).pack(expand=True)

        # ── Body ──────────────────────────────────────────────────────────────
        body = ctk.CTkFrame(self, fg_color="white", corner_radius=0)
        body.pack(fill="both", expand=True, padx=0, pady=0)

        ctk.CTkLabel(
            body,
            text="This software requires a valid license.\n"
                 "Please activate it before you can continue.",
            font=ctk.CTkFont(size=13),
            text_color="#333333",
            justify="center",
        ).pack(pady=(24, 8))

        # Device ID section
        did_frame = ctk.CTkFrame(body, fg_color=self.THEME_LIGHT, corner_radius=8)
        did_frame.pack(fill="x", padx=40, pady=8)

