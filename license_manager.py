# ── Cryptography ─────────────────────────────────────────────────────────────
try:
    from cryptography.hazmat.primitives import hashes, serialization
    from cryptography.hazmat.primitives.asymmetric import ec
    from cryptography.hazmat.backends import default_backend
    from cryptography.exceptions import InvalidSignature
    from cryptography.hazmat.primitives.ciphers.aead import AESGCM
    from cryptography.hazmat.primitives.kdf.pbkdf2 import PBKDF2HMAC
    _CRYPTO_OK = True
except ImportError:
    _CRYPTO_OK = False

# ── App constants ─────────────────────────────────────────────────────────────
_APP_SALT = b"GST_RECON_GSCINTIME_2026_PROD_v1"   # unique per product line
_ACTIVATION_DIR = Path(os.path.expandvars(r"%APPDATA%\GST_Reconciliation_Tool"))
_ACTIVATION_FILE = _ACTIVATION_DIR / "activation.dat"

# ── Embedded ECDSA P-256 public key (split to resist simple text search) ──────
# Generated by: python _license_keygen.py --setup
# To replace: split the PEM body into 3 equal parts and assign below.
_PUB_SEG_A = "MFkwEwYHKoZIzj0CAQYIKoZIzj0DAQcDQgAE"
_PUB_SEG_B = "0PqKxUzrFasDh4Ss4aDoYnz855wQHwFfZDds"
_PUB_SEG_C = "r3M4/7gNlthrrzkRFll/DnVf9QUj3E28qiriVP9kUXcnSty3fQ=="

def _get_public_key():
    """Reconstruct and load the embedded public key."""
    pem = (
        b"-----BEGIN PUBLIC KEY-----\n"
        + (_PUB_SEG_A + _PUB_SEG_B + _PUB_SEG_C).encode()
        + b"\n-----END PUBLIC KEY-----\n"
    )
    return serialization.load_pem_public_key(pem, backend=default_backend())


# ── Device fingerprinting ─────────────────────────────────────────────────────

def _reg_value(hive, path, name):
    """Read a Windows registry string value safely."""
    try:
        key = winreg.OpenKey(hive, path)
        value, _ = winreg.QueryValueEx(key, name)
        winreg.CloseKey(key)
        return str(value).strip()
    except Exception:
        return ""


def _volume_serial() -> str:
    """
    Return the C: volume serial via the Windows kernel32 API.
    Uses ctypes — no subprocess, no console, no encoding issues.
    Consistent in both normal Python and PyInstaller windowed mode.
    """
    try:
        serial = ctypes.c_uint32(0)
        ctypes.windll.kernel32.GetVolumeInformationW(
            "C:\\",
            None, 0,
            ctypes.byref(serial),
            None, None, None, 0,
        )
        return f"{serial.value:08X}"
    except Exception:
        return ""


def _raw_fingerprint():
    """
    Collect stable hardware identifiers.
    Uses only registry reads and Windows API — no subprocess,
    so the result is identical in script mode and PyInstaller windowed mode.
    """
    machine_guid = _reg_value(
        winreg.HKEY_LOCAL_MACHINE,
        r"SOFTWARE\Microsoft\Cryptography",
        "MachineGuid"
    )
    cpu_id = _reg_value(
        winreg.HKEY_LOCAL_MACHINE,
        r"HARDWARE\DESCRIPTION\System\CentralProcessor\0",
        "Identifier"        # e.g. "Intel64 Family 6 Model 158 Stepping 10"
    )
    vol_serial = _volume_serial()
    return f"{machine_guid}|{cpu_id}|{vol_serial}"


def get_device_fingerprint() -> str:
    """Return a SHA-256 hex digest of hardware identifiers + app salt."""
    raw = _raw_fingerprint()
    h = hashlib.sha256(_APP_SALT + raw.encode()).hexdigest()
    return h


def get_device_id() -> str:
    """Return the 16-char uppercase Device ID shown to users."""
    return get_device_fingerprint()[:16].upper()


# ── Activation storage (AES-256-GCM) ─────────────────────────────────────────

def _derive_aes_key(fingerprint: str) -> bytes:
    """Derive a 256-bit AES key from the device fingerprint via PBKDF2."""
    kdf = PBKDF2HMAC(
        algorithm=hashes.SHA256(),
        length=32,
        salt=_APP_SALT,
        iterations=200_000,
        backend=default_backend(),
    )
    return kdf.derive(fingerprint.encode())


def _save_activation(license_data: dict):
    """Encrypt and persist activation data to %APPDATA%."""
    _ACTIVATION_DIR.mkdir(parents=True, exist_ok=True)
    fingerprint = get_device_fingerprint()
    aes_key = _derive_aes_key(fingerprint)
    aesgcm = AESGCM(aes_key)
    nonce = os.urandom(12)
    plaintext = json.dumps(license_data, sort_keys=True).encode()
    ciphertext = aesgcm.encrypt(nonce, plaintext, _APP_SALT)
    with open(_ACTIVATION_FILE, "wb") as f:
        f.write(nonce + ciphertext)


def _load_activation() -> dict | None:
    """Decrypt and return activation data; returns None on any failure."""
    if not _ACTIVATION_FILE.exists():
        return None
    try:
        with open(_ACTIVATION_FILE, "rb") as f:
            data = f.read()
        if len(data) < 28:   # 12-byte nonce + 16-byte GCM tag minimum
            return None
        nonce, ciphertext = data[:12], data[12:]
        fingerprint = get_device_fingerprint()
        aes_key = _derive_aes_key(fingerprint)
        aesgcm = AESGCM(aes_key)
        plaintext = aesgcm.decrypt(nonce, ciphertext, _APP_SALT)
        return json.loads(plaintext.decode())
    except Exception:
        return None


# ── License file verification ─────────────────────────────────────────────────

def _verify_license_file(path: str) -> dict | None:
    """
    Load a .gstlic file, verify the ECDSA signature, and check that the
    device fingerprint matches.  Returns the license dict on success, None
    on any failure.
    """
    if not _CRYPTO_OK:
        messagebox.showerror(
            "Missing dependency",
            "The 'cryptography' package is not installed.\n"
            "Please contact support."
        )
        return None

    try:
        with open(path, "r", encoding="utf-8") as f:
            lic = json.load(f)
    except Exception:
        return None

    # Extract and decode signature
    try:
        sig_b64 = lic.pop("signature", None)
        if not sig_b64:
            return None
        sig = base64.b64decode(sig_b64)
    except Exception:
        return None

    # Re-create the exact payload that was signed
    payload = json.dumps(lic, sort_keys=True).encode()

    # Restore signature so the dict is complete on success
    lic["signature"] = sig_b64

    # Verify ECDSA signature
    try:
        pub_key = _get_public_key()
        pub_key.verify(sig, payload, ec.ECDSA(hashes.SHA256()))
    except (InvalidSignature, Exception):
        return None

    # Verify device binding
    expected_device_id = get_device_id()
    if lic.get("device_id", "").upper() != expected_device_id.upper():
        return None

    # Check expiry (if present)
    expires = lic.get("expires")
    if expires:
        try:
            exp_date = datetime.strptime(expires, "%Y-%m-%d").date()
            if exp_date < date.today():
                return None          # license expired
        except Exception:
            return None

    return lic


# ── Public API ────────────────────────────────────────────────────────────────

def is_activated() -> bool:
    """Return True if a valid, device-matching activation exists."""
    if not _CRYPTO_OK:
        return False
    activation = _load_activation()
    if not activation:
        return False
    # Confirm the stored device fingerprint still matches this machine
    if activation.get("device_id", "").upper() != get_device_id().upper():
        return False
    # Confirm expiry
    expires = activation.get("expires")
    if expires:
        try:
            exp_date = datetime.strptime(expires, "%Y-%m-%d").date()
            if exp_date < date.today():
                return False
        except Exception:
            return False
    return True


def activate_from_file(path: str) -> tuple[bool, str]:
    """
    Attempt to activate using a .gstlic file.
    Returns (success: bool, message: str).
    """
    lic = _verify_license_file(path)
    if lic is None:
        return False, (
            "License verification failed.\n\n"
            "Possible reasons:\n"
            "  • The license file is invalid or corrupted\n"
            "  • The license was issued for a different device\n"
            "  • The license has expired\n\n"
            "Please contact support with your Device ID."
        )
    try:
        _save_activation(lic)
        return True, f"Activation successful!\nWelcome, {lic.get('customer_name', 'User')}."
    except Exception as e:
        return False, f"Could not save activation:\n{e}"


# ── Activation dialog UI ──────────────────────────────────────────────────────

class ActivationDialog(ctk.CTkToplevel):
    """
    Modal dialog shown on first launch (or when license is invalid).
    Displays the Device ID and lets the user load a .gstlic file.
    """

