"""
License Manager for GST Reconciliation Tool
============================================
Offline, device-bound license verification system.

SECURITY ARCHITECTURE
---------------------
- Licenses are ECDSA P-256 signed JSON files (.gstlic)
- Each license encodes the target device fingerprint — it physically
  cannot activate on any other machine
- Activation state is persisted encrypted (AES-256-GCM) in %APPDATA%,
  keyed from the hardware fingerprint; copying the file to another PC
  makes decryption fail
- The private signing key is NEVER distributed; only the matching
  public key is compiled into this module
- Tampering with the activation file invalidates the GCM auth tag
  and forces re-activation

FIRST-TIME SETUP (developer)
----------------------------
1. python _license_keygen.py --setup          # generates _private_key.pem
2. Copy the printed public key into _PUB_SEGS below, replacing the
   placeholder segments
3. Keep _private_key.pem safe and SECRET — never commit or distribute it

LICENSE ISSUANCE (per customer)
--------------------------------
1. Customer runs the app → sees their Device ID (16-char hex string)
2. Customer emails Device ID to you
3. python _license_keygen.py --issue --device-id XXXX --customer "Name"
   → produces customer_license.gstlic
4. Send the .gstlic file to the customer
5. Customer opens the app → "Load License File" → selects .gstlic → Activate
"""

